package javaAssignments;

public class ifElseIfBlock {
	
	public static void main(String[] args) {
		
		int p = 100;
		int q = 200;
		int r = 300;
		
		if(p==q)
		{
			System.out.println("Rose");
		}
		else if(p!=r)
		{
			System.out.println("Orchid");
		}
		else if(p==r)
		{
			System.out.println("Jasmine");
		}
		else
		{
			System.out.println("Nothing");
		}
	}

}
